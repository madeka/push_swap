int	ft_strfirst(const char *str, int (*f)(int c))
{
	int	i;

	i = 0;
	while (str[i] != '\0')
	{
		if ((*f)(str[i]) == 1)
			return (i);
		else
			i++;
	}
	return (-1);
}
