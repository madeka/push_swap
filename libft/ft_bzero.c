#include "libft.h"

void	ft_bzero(void *s, size_t n)
{
	char		*s2;
	size_t		i;

	s2 = s;
	i = 0;
	if (s != NULL)
	{
		while (i < n)
		{
			s2[i] = 0;
			i++;
		}
	}
}
