#include <string.h>

char	*ft_strstr(const char *s1, const char *s2)
{
	size_t i;

	i = 0;
	if (*s2 == '\0')
		return ((char *)s1);
	else
	{
		while (*s1 != '\0')
		{
			if (*s1 == *s2)
			{
				while (*(s1 + i) == *(s2 + i))
				{
					i++;
					if (*(s2 + i) == '\0')
						return ((char *)s1);
				}
			}
			s1++;
		}
	}
	return (NULL);
}
